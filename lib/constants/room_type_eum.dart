/// Enumeration of room types commonly found in residential and architectural contexts.
enum RoomType {
  /// Represents all types of rooms without specific categorization.
  all,

  /// Represents a living room, a space designed for relaxation and socializing.
  living,

  /// Represents a bedroom, a private space for sleeping and rest.
  bed,

  /// Represents a bathroom, a room for personal hygiene activities.
  bath,
}
